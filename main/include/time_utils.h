// ============================================================================
// 时间工具函数 - 使用64位时间戳防止溢出
// 创建日期: 2026-06-02
//
// 问题: 原uint32_t时间戳49天后溢出 (2^32 ms = 49.7天)
// 方案: 改用uint64_t，584,942,417年后才溢出
//
// 使用说明:
//   - time_ms_64() 获取当前时间戳（毫秒）
//   - time_reached_64() 判断是否到达截止时间
//   - time_after_64() 判断时间a是否在b之后
// ============================================================================

#pragma once

#include <stdint.h>
#include <stdbool.h>
#include "freertos/FreeRTOS.h"
#include "freertos/task.h"

// ============================================================================
// 获取当前时间戳（毫秒） - 64位版本
//
// 返回: 自系统启动以来的毫秒数（64位）
// 注意: 此函数线程安全，可在任何上下文调用
// ============================================================================
static inline uint64_t time_ms_64(void)
{
    return (uint64_t)xTaskGetTickCount() * (uint64_t)portTICK_PERIOD_MS;
}

// ============================================================================
// 判断是否到达截止时间
//
// 参数:
//   now      - 当前时间戳
//   deadline - 截止时间戳
//
// 返回: true=已到达或超过截止时间, false=未到达
//
// 使用场景: 定时器、超时检测
// 示例: if (time_reached_64(now, timeout_at)) { /* 超时处理 */ }
// ============================================================================
static inline bool time_reached_64(uint64_t now, uint64_t deadline)
{
    // 使用有符号比较，即使发生溢出也能正确处理
    // 但在64位时间戳下，溢出几乎不可能发生
    return (int64_t)(now - deadline) >= 0;
}

// ============================================================================
// 判断时间a是否在时间b之后
//
// 参数:
//   a - 时间戳a
//   b - 时间戳b
//
// 返回: true=a在b之后, false=a在b之前或相等
//
// 使用场景: 事件排序、时间比较
// ============================================================================
static inline bool time_after_64(uint64_t a, uint64_t b)
{
    return (int64_t)(a - b) > 0;
}

// ============================================================================
// 计算时间差（防止溢出）
//
// 参数:
//   later   - 较晚的时间戳
//   earlier - 较早的时间戳
//
// 返回: 时间差（毫秒）
//
// 注意: 如果later < earlier，返回0
// ============================================================================
static inline uint64_t time_diff_64(uint64_t later, uint64_t earlier)
{
    if (later >= earlier) {
        return later - earlier;
    }
    return 0;
}
